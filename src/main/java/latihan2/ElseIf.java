package latihan2;

public class ElseIf {
    public static void main(String[] args) {
        int angka1 = 10;
        int angka2 = 10;

        if(angka1 > angka2){
            System.out.println("lebih besar");
        }else if(angka1 == angka2){
            System.out.println("Sama");
        }else{
            System.out.println("Lebih kecil");
        }
    }
}
